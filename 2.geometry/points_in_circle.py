# https://leetcode.com/problems/queries-on-number-of-points-inside-a-circle/
from typing import List


class Solution:
    def countPoints(
        self, points: List[List[int]], queries: List[List[int]]
    ) -> List[int]:
        result = []
        for x, y, r in queries:
            count = 0
            for a, b in points:
                if ((x - a) ** 2 + (y - b) ** 2) ** (1/2) <= r:
                    count += 1
            result.append(count)
        return result


